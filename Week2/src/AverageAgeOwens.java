
public class AverageAgeOwens {
	//Bradley Owens
	public static void main(String[] args) {
		int bradleyAge = 19;
		int jennyAge = 44;
		int chuckAge = 45;
		int tammeraAge = 15;
		int braydenAge = 13;
		double averageAge;
		
		averageAge = (double) (bradleyAge + jennyAge + chuckAge + tammeraAge + braydenAge) / 5;
		
		System.out.println("Bradley's age is " + bradleyAge);
		System.out.println("Jenny's age is " + jennyAge);
		System.out.println("Chuck's age is " + chuckAge);
		System.out.println("Tammera's age is " + tammeraAge);
		System.out.println("Brayden's age is " + braydenAge);
		System.out.println("The average age is " + averageAge);
	}
	
}
